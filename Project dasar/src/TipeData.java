import jdk.swing.interop.SwingInterOpUtils;

/*
* Tipe data primitive/fundamental di Java
* Integer, Byte, Short, Long
* Double, Float, Char, Boolean
*/
public class TipeData
{
    public static void main(String[] args)
    {
        //Integer
        int i = 10;
        System.out.println("=====INTEGER=====");
        System.out.println("Tipe data integer berfungsi untuk ");
        System.out.println("Nilai variabel i adalah " + i);
        //byte (Huruf "b" nya kecil)
        byte j = 10;
        System.out.println("=====BYTE=====");
        System.out.println("Tipe data byte digunakan untuk ");
        System.out.println("Nilai variabel j adalah " + j);
        //Short
        short k = 10;
        System.out.println("=====SHORT=====");
        System.out.println("Tipe data Short untuk ");
        System.out.println("Nilai dari variabel k adalah " + k);
        //Long
        long l = 10;
        System.out.println("=====LONG=====");
        System.out.println("Tipe data Long untuk ");
        System.out.println("Nilai dari variabel l adalah " + l);
        //Double
        double m = 10.5d;
        System.out.println("=====DOUBLE=====");
        System.out.println("Tipe data Double untuk bilangan koma");
        System.out.println("Nilai dari variabel m adalah " + m);
        //Float
        float n = -8.5f;
        System.out.println("=====FLOAT=====");
        System.out.println("Tipe data Float untuk bilangan real");
        System.out.println("Nilai dari variabel n adalah " + n);
    }
}
